package android2.team2.exe4;

public class BaiHat {
    public String tenBaiHat;
    public Integer fileBaiHat;

    public BaiHat(String tenBaiHat, Integer fileBaiHat) {
        this.tenBaiHat = tenBaiHat;
        this.fileBaiHat = fileBaiHat;
    }

    public void setTenBaiHat(String tenBaiHat) {
        this.tenBaiHat = tenBaiHat;
    }

    public void setFileBaiHat(Integer fileBaiHat) {
        this.fileBaiHat = fileBaiHat;
    }

    public String getTenBaiHat() {
        return tenBaiHat;
    }

    public Integer getFileBaiHat() {
        return fileBaiHat;
    }
}
